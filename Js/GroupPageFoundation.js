export function SetGroupPage(workId) {

   
        // 找到對應資料
        const workData = works.find(w => w.id === workId);
    
        if (workData) {


          const descContainer = document.getElementById("highlight-Text");
          descContainer.innerHTML = ""; // 清空舊內容

          workData.highlight_Desc.forEach(text => {
          const p = document.createElement("p");
          const span = document.createElement("span");
          span.textContent = text;
          p.appendChild(span);
          descContainer.appendChild(p);
          });  

          const descContainer2 = document.getElementById("Intro-Text");
          descContainer2.innerHTML = ""; // 清空舊內容

          workData.Intro_Desc.forEach(text => {
          const p = document.createElement("p");
          const span = document.createElement("span");
          span.textContent = text;
          p.appendChild(span);
          descContainer2.appendChild(p);
          }); 

          const memberContainer2 = document.getElementById("Mumber-Image");
          memberContainer2.innerHTML = ""; // 清空舊內容
          workData.Menber_image.forEach(img => {
            const imgElement = document.createElement("img");
            imgElement.classList.add("MumberImage");
            imgElement.src = img;
          
            memberContainer2.appendChild(imgElement);
          }); 


          document.getElementById("Group-Title").textContent = workData.title ;

          document.getElementById("Intro-Image").src = workData.intro_image;
          document.getElementById("Highlight-Image").src = workData.Highlight_image;

          // 底下是輪播圖部份的變更
          const imageContainer = document.getElementById("picture_slides");
          imageContainer.innerHTML = ""; 

          workData.Loop_image.forEach((src, index) => {
          const div = document.createElement("div");
          div.classList.add("picture_mySlides");

          const img = document.createElement("img");
          img.src = src; // 自訂 img 的 src
          img.alt = `圖片${index + 1}`;
    
          div.appendChild(img);
          imageContainer.appendChild(div);
          }); 
            
          
       
        } 
        // else {
        //   alert("找不到該作品資料");
        // }
}
